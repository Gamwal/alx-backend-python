#!/usr/bin/env python3
"""
Basic Annotations - concat
"""


def concat(str1: str, str2: str) -> str:
    """
    type-annotated function concat

    Args:
        str1 (str): first string to be concatenated
        str2 (str): second string to be concatenated

    Returns:
        sum (float): addition of a and b
    """
    return str1 + str2
